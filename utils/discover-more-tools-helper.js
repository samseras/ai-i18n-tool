/**
 * 获取过滤掉当前页面的工具列表数据
 * @param {Object} masterData 原始数据对象 (如 marketingToolsData)
 * @param {string} currentPath 当前页面的路径 (用于过滤)
 * @returns {Object} 过滤后的数据对象
 */
export const getFilteredDiscoverMoreTools = (masterData, currentPath) => {
  if (!masterData || !masterData.list) return masterData;

  // 归一化路径，移除可能的语言前缀 (如 /zh/ 或 /zh) 和开头的 /
  const normalize = (path) => {
    if (!path) return "";
    return path.replace(/^\/(zh|en|pt|fr|es|ja)(\/|$)/, "/").replace(/^\//, "");
  };

  const normalizedCurrentPath = normalize(currentPath);

  return {
    ...masterData,
    list: masterData.list.filter((item) => {
      const normalizedItemPath = normalize(item.path);
      return normalizedItemPath !== normalizedCurrentPath;
    }),
  };
};
